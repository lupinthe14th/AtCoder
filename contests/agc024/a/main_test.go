func TestSolution(t *testing.T) {
	t.Parallel()
	type in struct {
		a, b, c, k int
	}
	tests := []struct {
		in   in
		want int
	}{
		{in: in{a: 1, b: 2, c: 3, k: 1}, want: 1},
	}
	for i, tt := range tests {
		i, tt := i, tt
		t.Run(fmt.Sprint(i), func(t *testing.T) {
			t.Parallel()
			got := solution(tt.in.a, tt.in.b, tt.in.c, tt.in.k)
			if got != tt.want {
				t.Fatalf("in: %v got: %v want: %v", tt.in, got, tt.want)
			}
		})
	}
}

