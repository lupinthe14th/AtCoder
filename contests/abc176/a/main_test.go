func TestSolution(t *testing.T) {
	t.Parallel()
	type in struct {
		n, x, t int
	}
	tests := []struct {
		in   in
		want int
	}{
		{in: in{n: 20, x: 12, t: 6}, want: 12},
		{in: in{n: 1000, x: 1, t: 1000}, want: 1000000},
	}
	for i, tt := range tests {
		i, tt := i, tt
		t.Run(fmt.Sprint(i), func(t *testing.T) {
			t.Parallel()
			got := solution(tt.in.n, tt.in.x, tt.in.t)
			if got != tt.want {
				t.Fatalf("in: %v got: %v want: %v", tt.in, got, tt.want)
			}
		})
	}
}

func Example_main() {
	fd, _ := os.Open(filepath.Join("testdata", "input.txt"))
	orgStdin := os.Stdin
	os.Stdin = fd
	defer func() {
		os.Stdin = orgStdin
		fd.Close()
	}()

	main()

	// Output: 1000000
}
