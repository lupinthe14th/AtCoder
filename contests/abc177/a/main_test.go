func TestSolution(t *testing.T) {
	t.Parallel()
	type in struct {
		d, t, s int
	}
	tests := []struct {
		in   in
		want string
	}{
		{in: in{d: 1000, t: 15, s: 80}, want: "Yes"},
		{in: in{d: 2000, t: 20, s: 100}, want: "Yes"},
	}
	for i, tt := range tests {
		i, tt := i, tt
		t.Run(fmt.Sprint(i), func(t *testing.T) {
			t.Parallel()
			got := solution(tt.in.d, tt.in.t, tt.in.s)
			if got != tt.want {
				t.Fatalf("in: %v got: %v want: %v", tt.in, got, tt.want)
			}
		})
	}
}

