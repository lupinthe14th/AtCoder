func TestSolution(t *testing.T) {
	t.Parallel()
	type in struct {
		r, g, b, n int
	}
	tests := []struct {
		in   in
		want int
	}{
		{in: in{r: 1, g: 2, b: 3, n: 4}, want: 4},
	}
	for i, tt := range tests {
		i, tt := i, tt
		t.Run(fmt.Sprint(i), func(t *testing.T) {
			t.Parallel()
			got := solution(tt.in.r, tt.in.g, tt.in.b, tt.in.n)
			if got != tt.want {
				t.Fatalf("in: %v got: %v want: %v", tt.in, got, tt.want)
			}
		})
	}
}

func Example_main() {
	fd, _ := os.Open(filepath.Join("testdata", "input.txt"))
	orgStdin := os.Stdin
	os.Stdin = fd
	defer func() {
		os.Stdin = orgStdin
		fd.Close()
	}()

	main()

	// Output: 87058
}
