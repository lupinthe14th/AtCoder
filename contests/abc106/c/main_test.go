func TestSolution(t *testing.T) {
	t.Parallel()
	type in struct {
		s string
		k int
	}
	tests := []struct {
		in   in
		want string
	}{
		{in: in{s: "1214", k: 4}, want: "2"},
		{in: in{s: "3", k: 157}, want: "3"},
		{in: in{s: "3", k: 1e18}, want: "3"},
	}
	for i, tt := range tests {
		i, tt := i, tt
		t.Run(fmt.Sprint(i), func(t *testing.T) {
			t.Parallel()
			got := solution(tt.in.s, tt.in.k)
			if got != tt.want {
				t.Fatalf("in: %v got: %v want: %v", tt.in, got, tt.want)
			}
		})
	}
}

func Example_main() {
	fd, _ := os.Open(filepath.Join("testdata", "input.txt"))
	orgStdin := os.Stdin
	os.Stdin = fd
	defer func() {
		os.Stdin = orgStdin
		fd.Close()
	}()

	main()

	// Output: 2
}
